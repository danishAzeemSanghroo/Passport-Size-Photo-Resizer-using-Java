    private void uploadPicButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uploadPicButtonActionPerformed
        JFileChooser fileChooser=new JFileChooser();
        fileChooser.showOpenDialog(null);
        File f=fileChooser.getSelectedFile();
        BufferedImage bImage = null;
        try{
            int scaledWidth=354;
            int scaledHeight=472;
            String path=f.getAbsolutePath();
            File imageFile = new File(path);
            BufferedImage inputImage = ImageIO.read(imageFile);
//            System.out.println(imageFile.getName());
            // creates output image
            
           BufferedImage outputImage = new BufferedImage(scaledWidth,
                scaledHeight, inputImage.getType());
            
        // scales the input image to the output image
        Graphics2D g2d = outputImage.createGraphics();
        g2d.drawImage(inputImage, 0, 0, scaledWidth, scaledHeight, null);
        g2d.dispose();
        
        ImageIcon imageIcon = new ImageIcon(outputImage);
        picLabel.setIcon(imageIcon);
        
        //give your directory path here
        String path1 = "F:\\7th Semester\\DIP\\Image Processing Project\\Pictures\\"+nameTextField.getText()+".jpg";
        ImageIO.write(outputImage, "jpg", new File(path1));
          
        }catch(Exception e){e.printStackTrace();}
        System.out.println("Images were written succesfully.");
    }//GEN-LAST:event_uploadPicButtonActionPerformed

