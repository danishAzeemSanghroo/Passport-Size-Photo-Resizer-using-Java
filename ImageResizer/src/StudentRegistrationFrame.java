private void addStudent(){

     
    String name = nameTextField.getText();
    String fname=fNameTextField.getText();
    String caste=casteTextField.getText();
    String  rollNo = rollNoTextField.getText();
    String phoneNo=phoneNoTextField.getText();
    String gender=(String)genderComboBox.getSelectedItem();
    

    try{
               	int rows=DatabseManager.addStudent(name,fname,caste,rollNo,phoneNo,gender);
	if(rows>=1){
	        	javax.swing.JOptionPane.showMessageDialog(this,rows+" Records Inserted."); 
		clear();
		getStudent();				
	}
    }catch(Exception e){
        e.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(this,"Error: "+e.getMessage()); 
    }

}   //end    
private void deleteStudent(){
        StudentBean bean = (StudentBean)stdList.getSelectedValue();
    if(bean==null)return;
    try{
           int rows = DatabseManager.deleteStudent(bean.getStdId());
           if(rows>=1){
           javax.swing.JOptionPane.showMessageDialog(this,rows+"Record Removed.");
           deleteImage();
           getStudent();
           clear();
           }
    }catch(Exception e){
        e.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(this,"Error: "+e.getMessage()); 
    }

}
