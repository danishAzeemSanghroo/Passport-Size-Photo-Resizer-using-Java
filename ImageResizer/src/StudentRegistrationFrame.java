    private void stdListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_stdListValueChanged
  StudentBean bean=(StudentBean)stdList.getSelectedValue();
   if(bean==null)return;

        try {
                        
            stdIdTextField.setText( ""+bean.getStdId());
            nameTextField.setText( bean.getName());
            fNameTextField.setText( bean.getFname());
            casteTextField.setText( bean.getCaste());
            rollNoTextField.setText( bean.getRollNo());
            phoneNoTextField.setText( bean.getPhoneNo());
            genderComboBox.setSelectedItem( bean.getGender());
            ImageIcon icon=getIcon(bean.getName()+".jpg");
            if(icon==null)
            {
                picLabel.setText("Image not found");
            }
            else
            {
                picLabel.setIcon(icon);
            }
        } catch (Exception ex) {
            Logger.getLogger(StudentRegistrationFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

       
  

    }//GEN-LAST:event_stdListValueChanged

